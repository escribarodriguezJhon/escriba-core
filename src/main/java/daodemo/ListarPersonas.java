    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String codigo = txtNombreApellido.getText();
        if (codigo.equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese un nombre / apellido");
        } else {
            try {
                List<Estudiante> personas = new PersonaDaoImpl().listarPersonasByNombre(codigo);

                model = new DefaultTableModel();
                model.addColumn("Codigo");
                model.addColumn("Serie");
                model.addColumn("Nombre");
                model.addColumn("Telefono");
                model.addColumn("email");

                personas.forEach((estudiante) -> {
                    model.addRow(new Object[]{estudiante.getCodigo(),
                        estudiante.getSerie(),
                        estudiante.getNombre(),
                        estudiante.getTelefono(),
                        estudiante.getEmail()});
                });

                jTable1.setModel(model);

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Ingrese un nombre / apellido valido");
            }
        }


    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String codigo = txtNombreProfesor.getText();
        if (codigo.equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese un nombre / apellido");
        } else {
            try {
                List<Curso> cursos = new PersonaDaoImpl().listarCursosByNombre(codigo);

                model = new DefaultTableModel();
                model.addColumn("Codigo");
                model.addColumn("Nombre");
                model.addColumn("Credito");

                cursos.forEach((curso) -> {
                    model.addRow(new Object[]{curso.getCodigo(),
                        curso.getNombre(),
                        curso.getCredito()});
                });

                jTable1.setModel(model);

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Ingrese un nombre / apellido valido");
            }
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed
    DefaultTableModel model = new DefaultTableModel();

    void listarPersonas() {

        List<Persona> personas = new PersonaDaoImpl().listarPersonas();

        // Columns names of the table (nombre, telefono, email)
        model.addColumn("Nombre");
        model.addColumn("Telefono");
        model.addColumn("Email");

        // Add rows to the table
        for (Persona persona : personas) {
            model.addRow(new Object[]{persona.getNombre(), persona.getTelefono(), persona.getEmail()});
        }

        // Set the table model
        jTable1.setModel(model);

    }

