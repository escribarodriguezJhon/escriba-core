    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String codigo = jTextField2.getText();
        if (codigo.equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese un codigo");
        } else {
            try {
                Estudiante estudiante = new Estudiante();
                estudiante = (Estudiante) new PersonaDaoImpl().buscarEstudiante(codigo);
                if (estudiante != null) {

                    Direccion direccion = new Direccion();
                    direccion = (Direccion) new PersonaDaoImpl().obtenerDireccion(estudiante);

                    model = new DefaultTableModel();
                    model.addColumn("calle");
                    model.addColumn("ciudad");
                    model.addColumn("estado");
                    model.addColumn("pais");
                    model.addColumn("codigo postal");

                    Object[] fila = new Object[5];

                    fila[0] = direccion.getCalle();
                    fila[1] = direccion.getCiudad();
                    fila[2] = direccion.getEstado();
                    fila[3] = direccion.getPais();
                    fila[4] = direccion.getCodigoPostal();

                    model.addRow(fila);

                    jTable1.setModel(model);

                } else {
                    JOptionPane.showMessageDialog(null, "Ingrese un codigo valido");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Ingrese un codigo valido");
            }
        }
    }


